package com.fh.entity.topology.carbinet;

public class CarbinetMenu {

	private String id;
	private String pId;
	private String level;
	private String name;
	private String pictureId;
	private String pictureName;
	private Integer pictureLeft = 0;
	private Integer pictureTop = 0;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getpId() {
		return pId;
	}

	public void setpId(String pId) {
		this.pId = pId;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPictureId() {
		return pictureId;
	}

	public void setPictureId(String pictureId) {
		this.pictureId = pictureId;
	}

	public String getPictureName() {
		return pictureName;
	}

	public void setPictureName(String pictureName) {
		this.pictureName = pictureName;
	}

	public Integer getPictureLeft() {
		return pictureLeft;
	}

	public void setPictureLeft(Integer pictureLeft) {
		this.pictureLeft = pictureLeft;
	}

	public Integer getPictureTop() {
		return pictureTop;
	}

	public void setPictureTop(Integer pictureTop) {
		this.pictureTop = pictureTop;
	}

}
